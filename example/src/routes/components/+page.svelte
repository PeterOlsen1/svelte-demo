    </p>
    <h3>
        What are the advantages of svelte components?
    </h3>
    <p>
        Svelte components allow you to have css, javascript, and HTML all in the same file, defined
        within their own individual tags. This greatly reduces the clutter of having separate HTML, css, and JS files.
    </p>
    <h3>
        What are features of svelte components?
    </h3>
    <p>
        There are a few funtions defined by Svelte that act similar to React lifecycle hooks.
        <br><br>
        onMount(): Run when the component is <b>added</b> to the DOM
        <br>
        onDestroy(): Runs when the component is <b>removed</b> from the DOM
    </p>
